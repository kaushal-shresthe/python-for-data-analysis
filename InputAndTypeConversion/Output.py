# Basic Output
print("Hello, Python!")  # Output: Hello, Python!

# Printing Multiple Values
name = "Kaushal"
age = 18
print("My name is", name, "and I am", age, "years old.")
# Output: My name is Kaushal and I am 18 years old.

# Using sep Parameter
print("2025", "05", "29", sep="-")
# Output: 2025-05-29

# Using end Parameter
print("Loading", end="...")
print("Done")
# Output: Loading...Done


print("Hello\nWorld")   # Hello (new line) World

