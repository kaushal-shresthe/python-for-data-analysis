# Print Statement
# Print a simple text message
print("Hello, my name is Kaushal Shrestha")

# Multiline Print Statement
# Method 1: Using Triple Quotes
# This allows printing multiple lines directly
print("""
Hello, Kaushal
How are you?
""")

# Method 2: Using \n (New Line Character)
# \n is used to break lines within a string
print("I am fine\nAnd you?")

# Line Continuation
# Long statements can be split using \ or inside parentheses.
x = 10 + 20 + 30 + 40\
+ 23 + 45

# or

y = (10 + 20 + 30 + 40
+ 23 + 45)

print(x)
print(y)

